package com.example.lab2;

public class Objects {

    public String text;
    public String detailText;

    public Objects(String text, String detailText) {
        this.text = text;
        this.detailText = detailText;
    }

    public String getText() {
        return text;
    }

    public String getDetailText() {
        return detailText;
    }
}
